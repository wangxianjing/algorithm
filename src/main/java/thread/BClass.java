public class BClass {
    String s;
    int age;

    public BClass(String s, int age) {
        this.s = s;
        this.age = age;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
